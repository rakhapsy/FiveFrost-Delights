    private void loadTransaksiKeluar() {
        model = new DefaultTableModel();
        model.addColumn("ID Transaksi");
        model.addColumn("Tanggal");
        model.addColumn("Nama Produk");
        model.addColumn("Pelanggan");
        model.addColumn("Qty Keluar");
        tabelTransaksiKeluar.setModel(model);

        try {
            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();

            String sql = "SELECT t.id_keluar, t.tanggal, b.nama_produk, p.nama_pelanggan, t.qty_keluar " +
                         "FROM transaksi_keluar t " +
                         "JOIN master_produk b ON t.id_produk = b.id_produk " +
                         "JOIN master_pelanggan p ON t.id_pelanggan = p.id_pelanggan " +
                         "ORDER BY t.tanggal DESC";

            ResultSet rs = stat.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_keluar"),
                    rs.getString("tanggal"),
                    rs.getString("nama_produk"),
                    rs.getString("nama_pelanggan"),
                    rs.getString("qty_keluar")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Menampilkan Data: " + e.getMessage());
        }
    }
