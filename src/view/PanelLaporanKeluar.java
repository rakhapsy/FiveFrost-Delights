    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        // TODO add your handling code here:
        if (inputTanggalAwal.getDate() == null || inputTanggalAkhir.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Silakan pilih Tanggal Awal dan Tanggal Akhir terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            Connection conn = KoneksiDB.getKoneksi();

            String path = "src/report/LaporanKeluar.jasper";
            File fileReport = new File(path);

            if (!fileReport.exists()) {
                JOptionPane.showMessageDialog(this, "Maaf, File Laporan tidak ditemukan di: " + path, "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            HashMap<String, Object> param = new HashMap<>();
            
            Date tanggalAwal = new java.sql.Date(inputTanggalAwal.getDate().getTime());
            Date tanggalAkhir = new java.sql.Date(inputTanggalAkhir.getDate().getTime());
            param.put("tanggalAwal", tanggalAwal);
            param.put("tanggalAkhir", tanggalAkhir);

            JasperPrint jp = JasperFillManager.fillReport(fileReport.getAbsolutePath(), param, conn);
            if (jp.getPages().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Data transaksi tidak ditemukan untuk periode tanggal tersebut.", 
                    "Informasi", 
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
                return; 
            }
            JasperViewer.viewReport(jp, false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mencetak laporan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCetakActionPerformed


