    private void loadProduk(String keyword) {
        model = new DefaultTableModel();
        model.addColumn("ID Produk");
        model.addColumn("Nama Produk");
        model.addColumn("Kategori");
        model.addColumn("Harga Satuan");
        model.addColumn("Stok");
        tabelProduk.setModel(model);
        StringBuilder sql =  new StringBuilder(
                "SELECT * " +
                "FROM master_produk WHERE 1=1 "
        );

        if (!keyword.isEmpty()) {
            sql.append("AND nama_produk LIKE '%" + keyword + "%' ");
        }

        sql.append("ORDER BY id_produk ASC");

        try {
            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql.toString());

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_produk"),
                    rs.getString("nama_produk"),
                    rs.getString("kategori"),
                    formatRupiah(rs.getDouble("harga_satuan")),
                    rs.getString("stok")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Menampilkan Data: " + e.getMessage());
        }
    }
    
    private String generateId() {
        String idBaru = "PRD-FFD-0001";
        try {
            java.sql.Connection conn = koneksi.KoneksiDB.getKoneksi();
            java.sql.Statement stat = conn.createStatement();

            String sql = "SELECT MAX(RIGHT(id_produk, 4)) AS no_terakhir FROM master_produk";
            java.sql.ResultSet rs = stat.executeQuery(sql);

            if (rs.next()) {
                String noTerakhir = rs.getString("no_terakhir");
                if (noTerakhir != null) {
                    int angka = Integer.parseInt(noTerakhir) + 1;
                    idBaru = String.format("PRD-FFD-%04d", angka);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return idBaru;
    }
    
    private String formatRupiah(double nominal) {
        return String.format("Rp %,.0f", nominal).replace(",", ".");
    }
    
    private String ambilAngka(String formatRupiah) {
        return formatRupiah.replaceAll("[^\\d]", "");
    }
