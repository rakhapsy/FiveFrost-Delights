    private void btnSimpanProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanProdukActionPerformed
        // TODO add your handling code here:
        try {
            String id = generateId();
            String nama = inputNamaProduk.getText();
            String kategori = inputKategoriProduk.getText();
            String harga = inputHargaProduk.getText();

            if(nama.isEmpty() || kategori.isEmpty() || harga.isEmpty()){
                JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
                return;
            }

            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();

            String sql = "INSERT INTO master_produk (id_produk, nama_produk, kategori, harga_satuan, stok) "
            + "VALUES ('" + id + "', '" + nama + "', '" + kategori + "', '" + harga + "', 0)";
            stat.executeUpdate(sql);

            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
            loadProduk("");

            inputNamaProduk.setText("");
            inputKategoriProduk.setText("");
            inputHargaProduk.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Disimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanProdukActionPerformed

    private void btnUbahProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahProdukActionPerformed
        // TODO add your handling code here:
        int baris = tabelProduk.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin diubah pada tabel terlebih dahulu!");
            return;
        }

        try {
            String id = tabelProduk.getValueAt(baris, 0).toString();

            String namaBaru = inputNamaProduk.getText();
            String kategoriBaru = inputKategoriProduk.getText();
            String hargaBaru = inputHargaProduk.getText();

            if(namaBaru.isEmpty() || kategoriBaru.isEmpty() || hargaBaru.isEmpty()){
                JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
                return;
            }

            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();

            String sql = "UPDATE master_produk SET "
            + "nama_produk = '" + namaBaru + "', "
            + "kategori = '" + kategoriBaru + "', "
            + "harga_satuan = '" + hargaBaru + "' "
            + "WHERE id_produk = '" + id + "'";

            stat.executeUpdate(sql);

            JOptionPane.showMessageDialog(this, "Data Berhasil Diubah!");

            loadProduk("");

            inputNamaProduk.setText("");
            inputKategoriProduk.setText("");
            inputHargaProduk.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Diubah: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahProdukActionPerformed

    private void btnHapusProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusProdukActionPerformed
        // TODO add your handling code here:
        int baris = tabelProduk.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin dihapus pada tabel!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if(confirm == JOptionPane.YES_OPTION){
            try {
                String id = tabelProduk.getValueAt(baris, 0).toString();
                Connection conn = KoneksiDB.getKoneksi();
                Statement stat = conn.createStatement();

                String sql = "DELETE FROM master_produk WHERE id_produk = '" + id + "'";
                stat.executeUpdate(sql);

                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                loadProduk("");

                inputNamaProduk.setText("");
                inputKategoriProduk.setText("");
                inputHargaProduk.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Data Gagal Dihapus! Pastikan barang ini tidak memiliki riwayat transaksi.");
            }
        }
    }//GEN-LAST:event_btnHapusProdukActionPerformed

