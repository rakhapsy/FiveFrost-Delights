    private void btnSimpanPelangganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanPelangganActionPerformed
        // TODO add your handling code here:
        try {
            String id = generateId();
            String nama = inputNamaPelanggan.getText();
            String telp = inputTelpPelanggan.getText();
            String alamat = inputAlamatPelanggan.getText();

            if(nama.isEmpty() || telp.isEmpty() || alamat.isEmpty()){
                JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
                return;
            }

            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();

            String sql = "INSERT INTO master_pelanggan (id_pelanggan, nama_pelanggan, no_telp, alamat) "
            + "VALUES ('" + id + "', '" + nama + "', '" + telp + "', '" + alamat + "')";
            stat.executeUpdate(sql);

            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
            loadPelanggan("");

            inputNamaPelanggan.setText("");
            inputTelpPelanggan.setText("");
            inputAlamatPelanggan.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Disimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanPelangganActionPerformed

    private void btnUbahPelangganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahPelangganActionPerformed
        // TODO add your handling code here:
        int baris = tabelPelanggan.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin diubah pada tabel terlebih dahulu!");
            return;
        }

        try {
            String id = tabelPelanggan.getValueAt(baris, 0).toString();

            String namaBaru = inputNamaPelanggan.getText();
            String telpBaru = inputTelpPelanggan.getText();
            String alamatBaru = inputAlamatPelanggan.getText();

            if(namaBaru.isEmpty() || telpBaru.isEmpty() || alamatBaru.isEmpty()){
                JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
                return;
            }

            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();

            String sql = "UPDATE master_pelanggan SET "
            + "nama_pelanggan = '" + namaBaru + "', "
            + "no_telp = '" + telpBaru + "', "
            + "alamat = '" + alamatBaru + "' "
            + "WHERE id_pelanggan = '" + id + "'";

            stat.executeUpdate(sql);

            JOptionPane.showMessageDialog(this, "Data Berhasil Diubah!");

            loadPelanggan("");

            inputNamaPelanggan.setText("");
            inputTelpPelanggan.setText("");
            inputAlamatPelanggan.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Diubah: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahPelangganActionPerformed

    private void btnHapusPelangganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusPelangganActionPerformed
        // TODO add your handling code here:
        int baris = tabelPelanggan.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin dihapus pada tabel!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if(confirm == JOptionPane.YES_OPTION){
            try {
                String id = tabelPelanggan.getValueAt(baris, 0).toString();
                Connection conn = KoneksiDB.getKoneksi();
                Statement stat = conn.createStatement();

                String sql = "DELETE FROM master_pelanggan WHERE id_pelanggan = '" + id + "'";
                stat.executeUpdate(sql);

                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                loadPelanggan("");

                inputNamaPelanggan.setText("");
                inputTelpPelanggan.setText("");
                inputAlamatPelanggan.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Data Gagal Dihapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusPelangganActionPerformed

