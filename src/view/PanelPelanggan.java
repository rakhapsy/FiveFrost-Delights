    private void loadPelanggan(String keyword) {
        model = new DefaultTableModel();
        model.addColumn("ID Pelanggan");
        model.addColumn("Nama Pelanggan");
        model.addColumn("No. Telp");
        model.addColumn("Alamat");
        tabelPelanggan.setModel(model);
        StringBuilder sql =  new StringBuilder(
                "SELECT * " +
                "FROM master_pelanggan WHERE 1=1 "
        );

        if (!keyword.isEmpty()) {
            sql.append("AND nama_pelanggan LIKE '%" + keyword + "%' ");
        }

        sql.append("ORDER BY id_pelanggan ASC");

        try {
            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql.toString());

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_pelanggan"),
                    rs.getString("nama_pelanggan"),
                    rs.getString("no_telp"),
                    rs.getString("alamat")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Menampilkan Data: " + e.getMessage());
        }
    }
    
    private String generateId() {
        String idBaru = "PLG-FFD-0001";
        try {
            java.sql.Connection conn = koneksi.KoneksiDB.getKoneksi();
            java.sql.Statement stat = conn.createStatement();

            String sql = "SELECT MAX(RIGHT(id_pelanggan, 4)) AS no_terakhir FROM master_pelanggan";
            java.sql.ResultSet rs = stat.executeQuery(sql);

            if (rs.next()) {
                String noTerakhir = rs.getString("no_terakhir");
                if (noTerakhir != null) {
                    int angka = Integer.parseInt(noTerakhir) + 1;
                    idBaru = String.format("PLG-FFD-%04d", angka);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return idBaru;
    }
