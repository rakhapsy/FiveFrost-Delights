    public String toTitleCase(String teks) {
        if (teks == null || teks.isEmpty()) {
            return teks;
        }

        StringBuilder hasil = new StringBuilder();
        String[] kataKata = teks.toLowerCase().split(" ");

        for (String kata : kataKata) {
            if (!kata.isEmpty()) {
                hasil.append(Character.toUpperCase(kata.charAt(0))).append(kata.substring(1)).append(" ");
            }
        }

        return hasil.toString().trim();
    }
