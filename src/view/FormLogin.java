    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());
        
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan Password tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;   
        }
        
        try {
            String hashedInput = HashUtil.hashPassword(password);
            
            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();
            String sql = "SELECT * FROM pengguna WHERE username='" + username + "' AND password='" + hashedInput + "'";
            ResultSet rs = stat.executeQuery(sql);

            if (rs.next()) {
                String role = rs.getString("role");
                String userTitle = toTitleCase(username);
                SessionManager session = SessionManager.getInstance();
                session.setUsername(userTitle);
                session.setRole(role);
                JOptionPane.showMessageDialog(this, "Login Berhasil! Selamat datang " + userTitle + " (" + role + ")");
                Dashboard dashboard = new Dashboard();
                dashboard.aturHakAkses(role);
                dashboard.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Username atau Password salah!", "Login Gagal", JOptionPane.ERROR_MESSAGE);
                txtPassword.setText("");
                txtUsername.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan sistem: " + e.getMessage());
        }
    }//GEN-LAST:event_btnLoginActionPerformed

