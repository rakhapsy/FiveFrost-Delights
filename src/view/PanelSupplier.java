    private void btnSimpanSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanSupplierActionPerformed
        // TODO add your handling code here:
        try {
            String id = generateId();
            String nama = inputNamaSupplier.getText();
            String telp = inputTelpSupplier.getText();
            String alamat = inputAlamatSupplier.getText();

            if(nama.isEmpty() || telp.isEmpty() || alamat.isEmpty()){
                JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
                return;
            }

            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();

            String sql = "INSERT INTO master_supplier (id_supplier, nama_supplier, no_telp, alamat) "
            + "VALUES ('" + id + "', '" + nama + "', '" + telp + "', '" + alamat + "')";
            stat.executeUpdate(sql);

            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
            loadSupplier("");

            kondisiAwal();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Disimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanSupplierActionPerformed

    private void btnUbahSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahSupplierActionPerformed
        // TODO add your handling code here:
        int baris = tabelSupplier.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin diubah pada tabel terlebih dahulu!");
            return;
        }

        try {
            String id = tabelSupplier.getValueAt(baris, 0).toString();

            String namaBaru = inputNamaSupplier.getText();
            String telpBaru = inputTelpSupplier.getText();
            String alamatBaru = inputAlamatSupplier.getText();

            if(namaBaru.isEmpty() || telpBaru.isEmpty() || alamatBaru.isEmpty()){
                JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
                return;
            }

            Connection conn = KoneksiDB.getKoneksi();
            Statement stat = conn.createStatement();

            String sql = "UPDATE master_supplier SET "
            + "nama_supplier = '" + namaBaru + "', "
            + "no_telp = '" + telpBaru + "', "
            + "alamat = '" + alamatBaru + "' "
            + "WHERE id_supplier = '" + id + "'";

            stat.executeUpdate(sql);

            JOptionPane.showMessageDialog(this, "Data Berhasil Diubah!");

            loadSupplier("");

            kondisiAwal();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Diubah: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahSupplierActionPerformed

    private void btnHapusSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusSupplierActionPerformed
        // TODO add your handling code here:
        int baris = tabelSupplier.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin dihapus pada tabel!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if(confirm == JOptionPane.YES_OPTION){
            try {
                String id = tabelSupplier.getValueAt(baris, 0).toString();
                Connection conn = KoneksiDB.getKoneksi();
                Statement stat = conn.createStatement();

                String sql = "DELETE FROM master_supplier WHERE id_supplier = '" + id + "'";
                stat.executeUpdate(sql);

                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                loadSupplier("");

                kondisiAwal();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Data Gagal Dihapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusSupplierActionPerformed

